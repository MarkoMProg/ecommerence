package com.matchme.model;

import jakarta.persistence.*;

import java.time.LocalDateTime;
import lombok.*;
@Entity
@Table(name = "events")
@Getter
@Setter
@NoArgsConstructor
@AllArgsConstructor
public class Event {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column
    private String name;

    @Column
    private String location;

    @Column
    private LocalDateTime time;
}
